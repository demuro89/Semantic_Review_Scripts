def random_headers():
    return {'User-Agent': choice(user_agent_list),'Accept':'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8'}

def get_scholar_page(page):
    query="https://scholar.google.it/scholar?start={}&q={}&hl=it&as_std={}&as_ylo={}&as_yhi={}&as_vis={}".format(
            (page - 1) * 10,
            "Microservices+Security+Approach",
            "1,5",
            2020,
            2020,
            "1 | Microservices Security Approach - Google Scholar"
    )
    print("Page Number", page)
    headers=random_headers()
    print(headers)
    r=requests.get(query,headers)
    soup = BeautifulSoup(r.text, 'html.parser')
    #tutti gli attributi href degli a figli di un h3 con classe gs_rt
    #h3s = soup.find_all("h3", {"class": "gs_rt"})
    h3s = soup.find_all("h3")
    #print(soup)
    for h3 in h3s:
        #print(h3)
        links = h3.findChildren("a" , recursive=False)
        for a in links:
            link=format(a['href'])
            print("Found: {}",link)
            linksfile.write(link)
            linksfile.write("\n")

